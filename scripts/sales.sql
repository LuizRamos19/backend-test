CREATE TABLE sales (
    id                              NUMBER GENERATED BY DEFAULT AS IDENTITY,
    account_id						VARCHAR2(50) NOT NULL,
    transaction_id					VARCHAR2(50) NOT NULL,
    transaction_amount_usd			NUMBER(12,2) NOT NULL,
    transaction_currency_code		VARCHAR2(50) NOT NULL,
    local_hour						NUMBER(10) NOT NULL,
    transaction_scenario			CHAR(1) NOT NULL,
    transaction_type				CHAR(1) NOT NULL,
    transaction_ip_address			NUMBER(12,3) NOT NULL,
    ip_state						VARCHAR2(50) NOT NULL,
    ip_postal_code					VARCHAR2(50) NOT NULL,
    ip_country						VARCHAR2(2) NOT NULL,
    is_proxy_ip						NUMBER(1) NOT NULL,
    browser_language				VARCHAR2(5) NOT NULL,
    payment_instrument_type			VARCHAR2(50) NOT NULL,
    card_type						VARCHAR2(50),
    payment_billing_postal_code		VARCHAR2(50),
    payment_billing_state			VARCHAR2(50),
    payment_billing_country_code	VARCHAR2(50),
    shipping_postal_code			VARCHAR2(50),
    shipping_state					VARCHAR2(50),
    shipping_country				VARCHAR2(50),
    cvv_verify_result				VARCHAR2(50),
    digital_item_count				NUMBER(10) NOT NULL,
    physical_item_count				NUMBER(10) NOT NULL,
    transaction_date_time			TIMESTAMP NOT NULL,
    PRIMARY KEY(id)
);